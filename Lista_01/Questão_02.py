# Questão 02 - Cálculo da Área do QUadrado

print("  *Calculo do dobro da área do quadrado*")

#Entrada do dado
side = float(input("Digite o tamanho do Lado do Quadrado em cm:"))

#Cálculo das Áreas
area1 = side ** 2 #Área
area2 = area1 * 2 #Dobro da Área

#Exibição das Áreas
print(f"Área do quadrado: {area1} cm")
print(f"Dobro da área do quadrado: {area2} cm")
